<template>
  <header class="bg-white border-b border-coffee-200 sticky top-0 z-30 shadow-sm">
    <div class="flex items-center justify-between px-6 py-4">
      <!-- Left Section -->
      <div class="flex items-center gap-4">
        <!-- Mobile Menu Button -->
        <button
          @click="$emit('toggleSidebar')"
          class="p-2 hover:bg-coffee-100 rounded-lg transition-colors lg:hidden"
        >
          <Menu class="w-6 h-6 text-coffee-600" />
        </button>

        <!-- Desktop Menu Button -->
        <button
          @click="$emit('toggleSidebar')"
          class="hidden lg:block p-2 hover:bg-coffee-100 rounded-lg transition-colors"
        >
          <Menu class="w-6 h-6 text-coffee-600" />
        </button>

        <!-- Search Bar -->
        <div class="relative hidden md:block">
          <Search class="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-coffee-400" />
          <input
            v-model="searchQuery"
            type="text"
            :placeholder="searchPlaceholder"
            class="pl-10 pr-4 py-2 w-96 bg-coffee-50 border border-coffee-200 rounded-xl focus:outline-none focus:ring-2 focus:ring-coffee-500 focus:border-transparent"
            @input="handleSearch"
            @keyup.enter="handleSearch"
          />
        </div>

        <!-- Mobile Search Button -->
        <button
          @click="showMobileSearch = !showMobileSearch"
          class="md:hidden p-2 hover:bg-coffee-100 rounded-lg transition-colors"
        >
          <Search class="w-6 h-6 text-coffee-600" />
        </button>
      </div>

      <!-- Right Section -->
      <div class="flex items-center gap-4">
        <!-- Quick Actions -->
        <div class="hidden lg:flex items-center gap-2">
          <button
            @click="$emit('quickOrder')"
            class="px-4 py-2 bg-gradient-to-r from-coffee-500 to-coffee-600 text-white rounded-xl font-semibold hover:shadow-lg transition-all flex items-center gap-2"
          >
            <Plus class="w-4 h-4" />
            Tạo đơn hàng
          </button>
        </div>

        <!-- Notifications -->
        <div class="relative">
          <button
            @click="toggleNotifications"
            class="relative p-2 hover:bg-coffee-100 rounded-lg transition-colors"
          >
            <Bell class="w-6 h-6 text-coffee-600" />
            <span v-if="unreadCount > 0" class="absolute top-1 right-1 w-2 h-2 bg-red-500 rounded-full"></span>
          </button>

          <!-- Notifications Dropdown -->
          <div
            v-if="showNotifications"
            class="absolute right-0 top-full mt-2 w-80 bg-white rounded-xl shadow-lg border border-coffee-200 z-50"
          >
            <div class="p-4 border-b border-coffee-200">
              <h3 class="font-semibold text-coffee-800">Thông báo</h3>
            </div>
            <div class="max-h-64 overflow-y-auto">
              <div
                v-for="notification in notifications"
                :key="notification.id"
                class="p-4 hover:bg-coffee-50 transition-colors border-b border-coffee-100 last:border-b-0"
              >
                <div class="flex items-start gap-3">
                  <div :class="notificationIconClasses(notification.type)" class="p-2 rounded-lg">
                    <component :is="getNotificationIcon(notification.type)" class="w-4 h-4" />
                  </div>
                  <div class="flex-1">
                    <p class="text-sm font-medium text-coffee-800">{{ notification.title }}</p>
                    <p class="text-xs text-coffee-600 mt-1">{{ notification.message }}</p>
                    <p class="text-xs text-coffee-400 mt-1">{{ formatTime(notification.timestamp) }}</p>
                  </div>
                </div>
              </div>
            </div>
            <div class="p-4 border-t border-coffee-200">
              <button class="text-sm text-coffee-600 hover:text-coffee-800 transition-colors">
                Xem tất cả thông báo
              </button>
            </div>
          </div>
        </div>

        <!-- User Menu -->
        <div class="relative">
          <button
            @click="toggleUserMenu"
            class="flex items-center gap-3 p-2 hover:bg-coffee-100 rounded-lg transition-colors"
          >
            <div class="w-8 h-8 rounded-full bg-gradient-to-br from-coffee-500 to-coffee-400 flex items-center justify-center text-white font-bold text-sm">
              {{ userInitials }}
            </div>
            <div class="hidden md:block text-left">
              <p class="text-sm font-semibold text-coffee-800">{{ user?.firstName }} {{ user?.lastName }}</p>
              <p class="text-xs text-coffee-600">{{ user?.role }}</p>
            </div>
            <ChevronDown class="w-4 h-4 text-coffee-600" />
          </button>

          <!-- User Dropdown -->
          <div
            v-if="showUserMenu"
            class="absolute right-0 top-full mt-2 w-48 bg-white rounded-xl shadow-lg border border-coffee-200 z-50"
          >
            <div class="py-2">
              <button
                @click="handleProfile"
                class="w-full px-4 py-2 text-left text-sm text-coffee-700 hover:bg-coffee-50 transition-colors flex items-center gap-2"
              >
                <User class="w-4 h-4" />
                Thông tin cá nhân
              </button>
              <button
                @click="handleSettings"
                class="w-full px-4 py-2 text-left text-sm text-coffee-700 hover:bg-coffee-50 transition-colors flex items-center gap-2"
              >
                <Settings class="w-4 h-4" />
                Cài đặt
              </button>
              <hr class="my-2 border-coffee-200">
              <button
                @click="handleLogout"
                class="w-full px-4 py-2 text-left text-sm text-red-600 hover:bg-red-50 transition-colors flex items-center gap-2"
              >
                <LogOut class="w-4 h-4" />
                Đăng xuất
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>

    <!-- Mobile Search -->
    <div v-if="showMobileSearch" class="px-6 pb-4 md:hidden">
      <div class="relative">
        <Search class="absolute left-3 top-1/2 -translate-y-1/2 w-5 h-5 text-coffee-400" />
        <input
          v-model="searchQuery"
          type="text"
          placeholder="Tìm kiếm..."
          class="w-full pl-10 pr-4 py-2 bg-coffee-50 border border-coffee-200 rounded-xl focus:outline-none focus:ring-2 focus:ring-coffee-500 focus:border-transparent"
          @input="handleSearch"
          @keyup.enter="handleSearch"
        />
      </div>
    </div>
  </header>
</template>

<script setup lang="ts">
import { computed, ref, onMounted, onUnmounted } from 'vue'
import {
  Menu,
  Search,
  Bell,
  Plus,
  ChevronDown,
  User,
  Settings,
  LogOut,
  AlertCircle,
  CheckCircle,
  Info,
  AlertTriangle
} from 'lucide-vue-next'
import type { User as UserType, Notification } from '@/types'

interface Props {
  user?: UserType | null
  searchPlaceholder?: string
}

const props = withDefaults(defineProps<Props>(), {
  searchPlaceholder: 'Tìm kiếm...'
})

const emit = defineEmits<{
  toggleSidebar: []
  search: [query: string]
  quickOrder: []
  profile: []
  settings: []
  logout: []
}>()

const searchQuery = ref('')
const showMobileSearch = ref(false)
const showNotifications = ref(false)
const showUserMenu = ref(false)

// Mock notifications data
const notifications = ref<Notification[]>([
  {
    id: '1',
    type: 'warning',
    title: 'Nguyên liệu sắp hết',
    message: 'Cà phê hạt Robusta chỉ còn 2kg',
    timestamp: new Date(Date.now() - 5 * 60 * 1000)
  },
  {
    id: '2',
    type: 'success',
    title: 'Đơn hàng hoàn thành',
    message: 'Đơn hàng #0012 đã được giao thành công',
    timestamp: new Date(Date.now() - 15 * 60 * 1000)
  },
  {
    id: '3',
    type: 'info',
    title: 'Khách hàng mới',
    message: 'Nguyễn Văn A vừa đăng ký thành viên',
    timestamp: new Date(Date.now() - 30 * 60 * 1000)
  }
])

const unreadCount = computed(() => notifications.value.length)

const userInitials = computed(() => {
  if (!props.user) return 'A'
  const firstName = props.user.firstName || ''
  const lastName = props.user.lastName || ''
  return (firstName.charAt(0) + lastName.charAt(0)).toUpperCase() || 'A'
})

const getNotificationIcon = (type: string) => {
  const iconMap = {
    success: CheckCircle,
    error: AlertCircle,
    warning: AlertTriangle,
    info: Info
  }
  return iconMap[type as keyof typeof iconMap] || Info
}

const notificationIconClasses = (type: string) => {
  const classMap = {
    success: 'bg-green-100 text-green-600',
    error: 'bg-red-100 text-red-600',
    warning: 'bg-yellow-100 text-yellow-600',
    info: 'bg-blue-100 text-blue-600'
  }
  return classMap[type as keyof typeof classMap] || 'bg-coffee-100 text-coffee-600'
}

const formatTime = (timestamp: Date): string => {
  const now = new Date()
  const diff = now.getTime() - timestamp.getTime()
  const minutes = Math.floor(diff / 60000)
  
  if (minutes < 1) return 'Vừa xong'
  if (minutes < 60) return `${minutes} phút trước`
  
  const hours = Math.floor(minutes / 60)
  if (hours < 24) return `${hours} giờ trước`
  
  const days = Math.floor(hours / 24)
  return `${days} ngày trước`
}

const handleSearch = () => {
  emit('search', searchQuery.value)
}

const toggleNotifications = () => {
  showNotifications.value = !showNotifications.value
  showUserMenu.value = false
}

const toggleUserMenu = () => {
  showUserMenu.value = !showUserMenu.value
  showNotifications.value = false
}

const handleProfile = () => {
  showUserMenu.value = false
  emit('profile')
}

const handleSettings = () => {
  showUserMenu.value = false
  emit('settings')
}

const handleLogout = () => {
  showUserMenu.value = false
  emit('logout')
}

const handleClickOutside = (event: MouseEvent) => {
  const target = event.target as HTMLElement
  if (!target.closest('.dropdown-menu')) {
    showNotifications.value = false
    showUserMenu.value = false
  }
}

onMounted(() => {
  document.addEventListener('click', handleClickOutside)
})

onUnmounted(() => {
  document.removeEventListener('click', handleClickOutside)
})
</script>
